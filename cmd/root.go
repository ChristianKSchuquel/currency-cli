
package cmd

import (
	"os"

	"github.com/spf13/cobra"
)

// rootCmd represents the base command when called without any subcommands
var rootCmd = &cobra.Command{
	Use:   "currency-cli",
	Short: "cli app that gets info about currencies",
	Long: `A simple cli apps that gets info about 150+ currencies.
Using https://github.com/fawazahmed0/currency-api`,
}

func Execute() {
	err := rootCmd.Execute()
	if err != nil {
		os.Exit(1)
	}
}

func init() {
	rootCmd.Flags().BoolP("toggle", "t", false, "Help message for toggle")
}
